#function to add two numbers

def calculateSum(x,y):
    return (x+y)

#function to subtract two numbers

def calculateSubtract(x,y):
    return (x-y)

#function to multiply two numbers

def calculateMultiply(x,y):
    return (x*y)

#function to divide two numbers

def calculateDivision(x,y):
    return (x/y)

#function to power two numbers

def calculatePower(x,y):
    return (x**y)
