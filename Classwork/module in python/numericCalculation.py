#function to add two numbers
def add(a,b):
    return a+b

#function to subtract two numbers
def subtract(a,b):
    return a-b

#function to multiply two numbers 
def multiply(a,b):
    return a*b

#function to divide two numbers
def division(a,b):
    return a/b

#function to find remainder 
def modulous(a,b):
    return a%b
